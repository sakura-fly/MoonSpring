package com.moonspring.model;

public class WebLogModel {
	public String realIp;
	public String ip;
	public String os;
	public String borwser;
	public String type;
	public String url;
	public String uri;
	public String pattam;
	public String time;

	public String getRealIp() {
		return realIp;
	}

	public void setRealIp(String realIp) {
		this.realIp = realIp;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getOs() {
		return os;
	}

	public void setOs(String os) {
		this.os = os;
	}

	public String getBorwser() {
		return borwser;
	}

	public void setBorwser(String borwser) {
		this.borwser = borwser;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getUri() {
		return uri;
	}

	public void setUri(String uri) {
		this.uri = uri;
	}

	public String getPattam() {
		return pattam;
	}

	public void setPattam(String pattam) {
		this.pattam = pattam;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}
}
